const EducationView: React.FC = () => {
    
  const contentSections = educationText.split('---');
  const mainTitleAndIntro = contentSections[0].split('\n');
  const mainTitle = mainTitleAndIntro[0];
  const introParagraphs = mainTitleAndIntro.slice(1).filter(p => p.trim() !== '');

  const sections = contentSections.slice(1).map(section => {
    const lines = section.trim().split('\n');
    const title = lines[0];
    const paragraphs = lines.slice(1).filter(p => p.trim() !== '');
    return { title, paragraphs };
  });

  return (
    <div className="max-w-4xl mx-auto bg-white dark:bg-slate-800 p-6 sm:p-8 rounded-lg shadow-lg dark:shadow-xl">
      <div className="flex items-center space-x-4 mb-6">
        <BookOpenIcon className="h-10 w-10 text-cyan-600 dark:text-cyan-400" />
        <div>
          <h2 className="text-3xl font-bold text-slate-900 dark:text-white">Eğitim Merkezi</h2>
          <p className="text-slate-500 dark:text-slate-400">Hızlı okuma ve göz sağlığı hakkında bilgi edinin.</p>
        </div>
      </div>

      <article className="prose prose-lg max-w-none text-slate-700 dark:text-slate-300">
        <h3 className="text-2xl font-bold text-cyan-600 dark:text-cyan-400 mt-6 mb-4">{mainTitle}</h3>
        {introParagraphs.map((p, i) => <p key={`intro-${i}`} className="mb-4 text-lg leading-relaxed">{p}</p>)}

        {sections.map((section, index) => (
          <section key={index}>
             <hr className="my-8 border-slate-200 dark:border-slate-700" />
             <h4 className="text-xl font-bold text-cyan-600 dark:text-cyan-400 mt-6 mb-3">{section.title}</h4>
             {section.paragraphs.map((paragraph, pIndex) => {
                 const trimmedParagraph = paragraph.trim();
                 if (trimmedParagraph.startsWith('* **')) {
                     const content = trimmedParagraph.substring(2); // remove '* '
                     const parts = content.split(':**');
                     if (parts.length >= 2) {
                         return (
                            <p key={`p-${index}-${pIndex}`} className="mb-4 text-lg leading-relaxed">
                                <strong className="text-slate-800 dark:text-slate-200">{parts[0].replace(/\*/g, '')}:</strong>
                                {parts.slice(1).join(':**')}
                            </p>
                         );
                     }
                 }
                return <p key={`p-${index}-${pIndex}`} className="mb-4 text-lg leading-relaxed">{paragraph}</p>
             })}
          </section>
        ))}
      </article>
    </div>
  );
};

export default EducationView;
